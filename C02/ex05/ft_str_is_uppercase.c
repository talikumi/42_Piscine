int		ft_str_is_uppercase(char *str)
{
	int flag;
	int i;

	i = 0;
	flag = 1;
	while (str[i] != 0)
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
		{
			flag = 0;
		}
		i++;
	}
	return (flag);
}
